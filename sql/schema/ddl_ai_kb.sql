/* Knowledge Base schema - Oracle style placeholder */

CREATE TABLE AI_REG_DOCUMENT (
    DOC_ID              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DOC_CATEGORY        VARCHAR2(50) NOT NULL,
    DOC_TITLE           VARCHAR2(300) NOT NULL,
    SOURCE_TYPE         VARCHAR2(50) NOT NULL,
    SOURCE_ORG          VARCHAR2(100),
    VERSION_NO          VARCHAR2(100),
    EFFECTIVE_FROM      DATE,
    EFFECTIVE_TO        DATE,
    CONFIDENTIAL_GRADE  VARCHAR2(30) DEFAULT 'PUBLIC' NOT NULL,
    APPROVAL_STATUS     VARCHAR2(30) DEFAULT 'PENDING' NOT NULL,
    FILE_HASH           VARCHAR2(64),
    CREATED_AT          DATE DEFAULT SYSDATE NOT NULL,
    ACTIVE_YN           CHAR(1) DEFAULT 'Y' NOT NULL
);

CREATE TABLE AI_REG_CLAUSE (
    CLAUSE_ID          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DOC_ID             NUMBER NOT NULL,
    CLAUSE_NO          VARCHAR2(100),
    CLAUSE_TITLE       VARCHAR2(300),
    RISK_CATEGORY      VARCHAR2(100),
    KEYWORDS           VARCHAR2(1000),
    CLAUSE_SUMMARY     CLOB,
    PRACTICAL_USE      CLOB,
    LIMITATION_NOTE    CLOB,
    CREATED_AT         DATE DEFAULT SYSDATE NOT NULL,
    ACTIVE_YN          CHAR(1) DEFAULT 'Y' NOT NULL,
    CONSTRAINT FK_AI_REG_CLAUSE_DOC FOREIGN KEY (DOC_ID) REFERENCES AI_REG_DOCUMENT (DOC_ID)
);
