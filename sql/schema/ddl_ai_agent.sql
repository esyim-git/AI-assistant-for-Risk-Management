/* AI Agent base schema - Oracle style placeholder */

CREATE TABLE AI_TASK_LOG (
    TASK_ID          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CREATED_AT       DATE DEFAULT SYSDATE NOT NULL,
    USER_ID          VARCHAR2(50) NOT NULL,
    TASK_TYPE        VARCHAR2(50) NOT NULL,
    TOOL_TYPE        VARCHAR2(20) NOT NULL,
    USER_REQUEST     CLOB NOT NULL,
    OUTPUT_TEXT      CLOB,
    MODEL_VER        VARCHAR2(50),
    PROMPT_VER       VARCHAR2(50),
    RULE_VER         VARCHAR2(50),
    SAFETY_RESULT    VARCHAR2(20),
    OUTPUT_HASH      VARCHAR2(64)
);

CREATE TABLE AI_FEEDBACK_LOG (
    FEEDBACK_ID      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TASK_ID          NUMBER NOT NULL,
    CREATED_AT       DATE DEFAULT SYSDATE NOT NULL,
    USER_ID          VARCHAR2(50) NOT NULL,
    FEEDBACK_CD      VARCHAR2(30) NOT NULL,
    FEEDBACK_COMMENT VARCHAR2(1000),
    FINAL_TEXT       CLOB,
    REVIEW_STATUS    VARCHAR2(20) DEFAULT 'PENDING',
    CONSTRAINT FK_AI_FEEDBACK_TASK FOREIGN KEY (TASK_ID) REFERENCES AI_TASK_LOG (TASK_ID)
);
