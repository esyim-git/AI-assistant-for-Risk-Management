/* NCR rule schema - Oracle style placeholder */

CREATE TABLE AI_NCR_RULE_SET (
    RULE_SET_ID        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    RULE_SET_NAME      VARCHAR2(200) NOT NULL,
    VERSION_NO         VARCHAR2(100) NOT NULL,
    EFFECTIVE_FROM     DATE NOT NULL,
    EFFECTIVE_TO       DATE,
    APPROVAL_STATUS    VARCHAR2(30) DEFAULT 'PENDING' NOT NULL,
    FORMULA_SUMMARY    CLOB,
    VALIDATION_NOTE    CLOB,
    CREATED_AT         DATE DEFAULT SYSDATE NOT NULL,
    ACTIVE_YN          CHAR(1) DEFAULT 'Y' NOT NULL
);

CREATE TABLE AI_NCR_COMPONENT_MAP (
    COMPONENT_ID       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    RULE_SET_ID        NUMBER NOT NULL,
    COMPONENT_NAME     VARCHAR2(200) NOT NULL,
    COMPONENT_TYPE     VARCHAR2(100) NOT NULL,
    SOURCE_SYSTEM      VARCHAR2(100),
    SOURCE_TABLE       VARCHAR2(200),
    SOURCE_COLUMN      VARCHAR2(200),
    SIGN_TYPE          VARCHAR2(20),
    CALC_LOGIC_DESC    CLOB,
    REG_BASIS          VARCHAR2(1000),
    CHECK_SQL_TEMPLATE CLOB,
    CREATED_AT         DATE DEFAULT SYSDATE NOT NULL,
    ACTIVE_YN          CHAR(1) DEFAULT 'Y' NOT NULL,
    CONSTRAINT FK_AI_NCR_COMPONENT_RULE FOREIGN KEY (RULE_SET_ID) REFERENCES AI_NCR_RULE_SET (RULE_SET_ID)
);
